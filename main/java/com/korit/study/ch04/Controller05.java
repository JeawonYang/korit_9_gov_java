package com.korit.study.ch04;

public class Controller05 {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println(i + 1);
        } for (int n = 5; n > 0; n--) {
            System.out.println(n);
        }

        for (int i = 0; i < 5; i++) {
            System.out.println(5 - i);
        }
    }
}
