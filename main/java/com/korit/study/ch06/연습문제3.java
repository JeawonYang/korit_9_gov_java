package com.korit.study.ch06;

public class 연습문제3 {
    public static void main(String[] args) {
        int result = squared (10);
        System.out.println(result);

        // 매개변수 있음, 리턴값 있음
        // 정수를 받아서 제곱값을 반환하는 함수를 작성하세요
    }
    public static int squared(int number) {
        return number * number;
    }
}
